_SQL_SYSTEM_HEADER = """\
You are a read-only analytics agent for an e-commerce data platform.
Your job is to answer questions about business performance by \
querying the Gold layer of the data warehouse using Amazon Athena SQL (Presto/Trino dialect).
Questions may be asked in any language. Always respond in the same language as the question.

## Rules you must follow

1. Only generate SELECT statements. Never use DROP, DELETE, INSERT, UPDATE, \
CREATE, ALTER, or TRUNCATE anywhere in the query, including inside CTEs or subqueries.
2. Only query tables in the {gold_database} database. Never reference Bronze, Silver, \
or any other database.
3. Always include a LIMIT clause. Never return more than {max_rows} rows.
4. Use Athena SQL syntax: single quotes for string literals, DATE 'YYYY-MM-DD' for \
date literals, CURRENT_DATE for today's date. Do not use MySQL-specific functions.
5. These are pre-aggregated Gold tables, not raw transaction tables. \
There is no raw orders, customers, or payments table — use the Gold aggregations directly.
6. State every assumption you make about how the question maps to the data.

## Output format

Always respond in exactly this structure — no text before or after:

<sql>
SELECT ...
FROM ...
LIMIT n
</sql>
<assumptions>
- Table: <name> — <reason for choosing this table>
- Filter: <what filter was applied and why, or 'none'>
- Note: <any interpretation of ambiguous terms in the question>
</assumptions>

## Gold tables available

{schema_text}
"""


def build_system_prompt(
    schemas: AllSchemas,
    gold_database: str,
    max_rows: int = 1000,
) -> str:
    """Build the Claude system prompt with all Gold table schemas embedded.

    Merges three layers of description for each table:
      1. Physical schema from Glue Catalog (column names + data types from SchemaResolver)
      2. Business context from dbt catalog.json (column descriptions, if pipeline has run)
      3. Static hints from GOLD_TABLE_CATALOG (accepted values, business meaning)

    If the live schema dict is empty (SchemaResolver could not reach Glue), falls back
    to the static catalog only — enough for Claude to function in offline/test mode.

    Args:
        schemas: AllSchemas dict from SchemaResolver.load_all_schemas().
        gold_database: Name of the Gold Glue database (e.g. 'edp_dev_gold').
        max_rows: Maximum rows Claude should return per query.

    Returns:
        Fully formatted system prompt string ready to pass to the Claude API.
    """
    schema_blocks: list[str] = []

    if schemas:
        for table_name, table_schema in schemas.items():
            block = _format_table_block(table_name, table_schema)
            schema_blocks.append(block)
    else:
        # Fallback: render from static catalog only (no live Glue data).
        for table_name, hints in GOLD_TABLE_CATALOG.items():
            block = _format_static_table_block(table_name, hints, gold_database)
            schema_blocks.append(block)

    schema_text = "\n\n".join(schema_blocks)

    return _SQL_SYSTEM_HEADER.format(
        gold_database=gold_database,
        max_rows=max_rows,
        schema_text=schema_text,
    )


def _format_table_block(table_name: str, table_schema: Any) -> str:
    """Format a live TableSchema, supplemented with static hints."""
    hints = GOLD_TABLE_CATALOG.get(table_name, {})

    # Prefer live dbt description; fall back to static hint.
    description = table_schema.description or hints.get("__table__", "")

    lines = [f"### {table_schema.database}.{table_name}"]
    if description:
        lines.append(description)
    if table_schema.partition_keys:
        lines.append(f"Partition keys: {', '.join(table_schema.partition_keys)}")
    lines.append("Columns:")

    for col in table_schema.columns:
        # Prefer dbt description; fall back to static hint for this column.
        col_desc = col.description or hints.get(col.name, "")
        suffix = f" — {col_desc}" if col_desc else ""
        lines.append(f"  {col.name} ({col.data_type}){suffix}")

    return "\n".join(lines)


def _format_static_table_block(
    table_name: str,
    hints: dict[str, str],
    gold_database: str,
) -> str:
    """Format a table block from the static catalog only (offline fallback)."""
    description = hints.get("__table__", "")
    lines = [f"### {gold_database}.{table_name}"]
    if description:
        lines.append(description)
    lines.append("Columns:")
    for col_name, col_desc in hints.items():
        if col_name == "__table__":
            continue
        lines.append(f"  {col_name} — {col_desc}")
    return "\n".join(lines)


