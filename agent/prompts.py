def build_sql_request_messages(question: str) -> list[dict[str, str]]:
    """Build the initial messages list for a SQL generation request.

    Returns a single user message containing the plain-English question.
    The system prompt is passed separately to the Claude API call.
    """
    return [{"role": "user", "content": question}]


def build_sql_correction_messages(
    prior_messages: list[dict[str, str]],
    prior_sql: str,
    prior_assumptions: list[str],
    validation_reason: str,
) -> list[dict[str, str]]:
    """Extend the conversation with a correction turn.

    Reconstructs the assistant's previous response from the parsed sql and
    assumptions, then appends a user correction message explaining what
    was wrong. The generator calls this when the validator rejects the SQL.

    Args:
        prior_messages: The messages list used in the previous call.
        prior_sql: The SQL that failed validation.
        prior_assumptions: The assumptions list from the previous response.
        validation_reason: The reason string from SQLValidationError.reason.

    Returns:
        A new messages list with the assistant response + correction appended.
    """
    assumptions_text = "\n".join(f"- {a}" for a in prior_assumptions)
    assistant_content = (
        f"<sql>\n{prior_sql}\n</sql>\n" f"<assumptions>\n{assumptions_text}\n</assumptions>"
    )

    correction_content = (
        f"The SQL you generated failed a guardrail check:\n\n"
        f"{validation_reason}\n\n"
        f"Please fix the SQL so it passes that check and try again. "
        f"Return the same <sql> / <assumptions> format."
    )

    return [
        *prior_messages,
        {"role": "assistant", "content": assistant_content},
        {"role": "user", "content": correction_content},
    ]


